"""
========================================================
題目：UVA 10812 — Beat the Spread!
來源：ZeroJudge a805
========================================================

【題目大意】
  超級盃賭局中，有人押注兩隊分數的「和 S」，有人押注「差 D」。
  給定 S 與 D，求兩隊各自的得分（較大分先輸出）。
  若無整數解或出現負數得分，則輸出 impossible。

【數學推導】
  設較大分為 a、較小分為 b，則：
      a + b = S   … (1)
      a - b = D   … (2)

  由 (1)+(2)：  2a = S + D  →  a = (S + D) / 2
  由 (1)-(2)：  2b = S - D  →  b = (S - D) / 2

【合法條件】
  1. (S + D) 必須為偶數，否則 a、b 不是整數
  2. b = (S - D) / 2 不得為負數，即 S >= D
"""

import sys


# ──────────────────────────────────────────────────────
# 核心邏輯函式（可被單元測試直接引入）
# ──────────────────────────────────────────────────────
def solve(s: int, d: int) -> str:
    """
    給定兩隊分數之和 s 與差 d，回傳答案字串。

    參數：
        s (int)：兩隊分數之和（Sum）
        d (int)：兩隊分數差的絕對值（Difference）

    回傳值：
        str：「較大分 較小分」 或 "impossible"
    """
    # ── 條件一：(s + d) 必須是偶數，才能除以 2 得到整數 ──
    if (s + d) % 2 != 0:
        return "impossible"

    big   = (s + d) // 2   # 較大分（高分隊）
    small = (s - d) // 2   # 較小分（低分隊）

    # ── 條件二：分數不得為負數（得分定義為非負整數）──
    if small < 0:
        return "impossible"

    # 以「較大分 較小分」的格式輸出
    return f"{big} {small}"


# ──────────────────────────────────────────────────────
# 主程式：讀取標準輸入，依序處理每組測試資料
# ──────────────────────────────────────────────────────
def main():
    input_data = sys.stdin.read().split()
    idx = 0

    # 第一行：測試資料組數 n
    n = int(input_data[idx]); idx += 1

    results = []
    for _ in range(n):
        s = int(input_data[idx]);     idx += 1   # 兩隊分數之和
        d = int(input_data[idx]);     idx += 1   # 兩隊分數差（絕對值）
        results.append(solve(s, d))

    # 一次性輸出（減少 I/O 次數，加快執行速度）
    print("\n".join(results))


if __name__ == "__main__":
    main()
