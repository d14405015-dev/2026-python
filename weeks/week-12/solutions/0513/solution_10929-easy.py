"""
========================================================
題目：UVA 10929 — 11 的倍數判斷（簡易好記版）
來源：ZeroJudge a922
========================================================

【這版的設計理念】
  讓程式像「逐步說明」一樣簡單直觀：

  「從右到左數每一位，奇數位加起來、偶數位加起來，
    然後看這兩個和的差是不是 11 的倍數。」

【一句話記憶】
  奇數位減偶數位，能被 11 整除就是倍數。

【例子】
  121：右邊算起
    位 1: 1（奇）→ 奇數位和 = 1
    位 2: 2（偶）→ 偶數位和 = 2  
    位 3: 1（奇）→ 奇數位和 = 1+1 = 2
    差 = 2 - 2 = 0，能被 11 整除 → 是 11 的倍數

  132：右邊算起
    位 1: 2（奇）→ 奇數位和 = 2
    位 2: 3（偶）→ 偶數位和 = 3
    位 3: 1（奇）→ 奇數位和 = 2+1 = 3
    差 = 3 - 3 = 0，能被 11 整除 → 是 11 的倍數
"""


def 計算奇數位和與偶數位和(數字字串):
    """
    從右到左掃描每一位，統計奇數位和偶數位的數字和
    
    奇數位：第 1, 3, 5... 位（從右往左計）
    偶數位：第 2, 4, 6... 位（從右往左計）
    
    返回 (奇數位和, 偶數位和)
    """
    # 初始化兩個和為 0
    奇數位和 = 0
    偶數位和 = 0
    
    # 從右到左遍歷，position 0 代表最右邊（位 1）
    for 位置, 數字字元 in enumerate(reversed(數字字串)):
        數字 = int(數字字元)
        
        # 位置是偶數表示奇數位（位 1, 3, 5...）
        # 位置是奇數表示偶數位（位 2, 4, 6...）
        if 位置 % 2 == 0:
            奇數位和 += 數字
        else:
            偶數位和 += 數字
    
    return 奇數位和, 偶數位和


def 判斷是否為11的倍數(數字字串):
    """
    判斷字串表示的數是否為 11 的倍數
    
    步驟：
      1. 計算奇數位和與偶數位和
      2. 求兩者的差
      3. 檢查差是否被 11 整除
    """
    # 取得奇數位和與偶數位和
    奇數位和, 偶數位和 = 計算奇數位和與偶數位和(數字字串)
    
    # 計算差
    差 = 奇數位和 - 偶數位和
    
    # 若差被 11 整除，該數是 11 的倍數
    return 差 % 11 == 0


def solve(數字字串):
    """
    主要求解函式，返回格式化後的判斷結果
    
    若為 11 的倍數 → 回傳 "N is a multiple of 11."
    否則 → 回傳 "N is not a multiple of 11."
    """
    if 判斷是否為11的倍數(數字字串):
        return f"{數字字串} is a multiple of 11."
    else:
        return f"{數字字串} is not a multiple of 11."


def main():
    """
    主程式入口
    
    輸入流程：
      - 讀取一行整數
      - 若為 0，終止
      - 否則輸出判斷結果
      - 重複
    """
    while True:
        # ── 讀入一行 ──────────────────
        輸入字串 = input().strip()
        
        # ── 檢查是否為終止符 ────────────
        if 輸入字串 == "0":
            break
        
        # ── 否則輸出判斷結果 ──────────────
        結果 = solve(輸入字串)
        print(結果)


if __name__ == "__main__":
    main()
