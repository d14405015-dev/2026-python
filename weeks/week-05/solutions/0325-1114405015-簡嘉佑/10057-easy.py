# UVA 10057 - A Weird Calculator（簡單好記版）
#
# ====== 題目核心觀念 ======
# 給 n 個整數，找一個 A 讓「所有元素與 A 的距離總和」最小。
# 也就是讓 |X1-A| + |X2-A| + ... + |Xn-A| 最小。
#
# ====== 關鍵觀念：中位數 ======
# 這是統計學裡的性質：
#   「絕對偏差總和」在 A = 中位數 時會最小。
#
# 直覺解釋：
#   把所有數字排好，放在數線上。
#   A 若比中間偏左，往右移一格可讓左側距離增加 1、右側距離減少 1，
#   只要左側人數 < 右側人數，往右移是有利的，直到 A 到達中間才停。
#   ─ 所以最佳位置就是「中間那個數」，即中位數。
#
# ====== 奇偶兩種情形 ======
# 排序後陣列（0-indexed）：X[0] ≤ X[1] ≤ … ≤ X[n-1]
#
# n 為奇數（例如 n=5）：
#   中位數唯一，位於 X[2]（索引 n//2 = 2）。
#   只有一種最佳 A，所以 poss = 1。
#
# n 為偶數（例如 n=4）：
#   沒有「唯一」中間元素，有「下中位數 X[1]」和「上中位數 X[2]」。
#   任何 X[1] ≤ A ≤ X[2] 的整數都能讓總和一樣小。
#   poss = X[2] - X[1] + 1（這個範圍內共幾個整數）。
#
# ====== 統一公式（奇偶都適用） ======
# 設：
#   lower_idx = (n-1) // 2   ← 下中位數索引
#   upper_idx = n // 2        ← 上中位數索引
#   A    = X[lower_idx]      ← 取最小可行值
#   cnt  = 陣列中等於 A 的個數（這些元素差為 0，是最小差）
#   poss = X[upper_idx] - X[lower_idx] + 1
#
# ====== 輸入輸出 ======
# 輸入：多組測資直到 EOF；每組先讀 n，再讀 n 個整數。
# 輸出：每組一行「A cnt poss」（空白分隔）。

import sys

# 一次讀入全部，切成數字 token，方便依序取值。
tokens = sys.stdin.read().split()
idx = 0

while idx < len(tokens):
    # ── 讀取本組輸入 ──────────────────────────────────────────────
    n = int(tokens[idx])   # 陣列長度
    idx += 1

    xs = []
    for _ in range(n):
        xs.append(int(tokens[idx]))   # 依序讀入 n 個整數
        idx += 1

    # ── 排序 ──────────────────────────────────────────────────────
    # 排序後才能用索引快速取得中位數。
    xs.sort()

    # ── 計算中位數索引 ────────────────────────────────────────────
    # 下中位數索引（奇偶數皆通用）：
    #   n=1 → 0, n=2 → 0, n=3 → 1, n=4 → 1, n=5 → 2
    lower = (n - 1) // 2

    # 上中位數索引（奇偶數皆通用）：
    #   n=1 → 0, n=2 → 1, n=3 → 1, n=4 → 2, n=5 → 2
    upper = n // 2

    # ── 計算三個答案 ──────────────────────────────────────────────
    # A 取下中位數（可行 A 的最小值）
    a = xs[lower]

    # cnt = 陣列中等於 A 的元素個數。
    # 因為 A 必為陣列中的值，所以每個等於 A 的元素差為 0（最小差）。
    cnt = xs.count(a)

    # poss = 上中位數與下中位數之間（含兩端）的整數個數。
    # n 為奇數時 upper == lower，所以 poss = 1（唯一最佳 A）。
    # n 為偶數時 upper > lower，poss > 1（一段範圍都是最佳 A）。
    poss = xs[upper] - xs[lower] + 1

    # ── 輸出本組結果 ─────────────────────────────────────────────
    print(f"{a} {cnt} {poss}")
