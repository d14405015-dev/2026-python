"""
========================================================
題目：UVA 10812 — Beat the Spread!（簡易好記版）
來源：ZeroJudge a805
========================================================

【這版的設計理念】
  讓程式碼像「逐步解數學題」一樣直觀：
  先把題目轉成方程式，再照公式填入，最後做兩個檢查。
  不需要背公式，只要記得「聯立方程式加減消去法」即可。

【一句話記憶法】
  「大分 = 和加差除以二；小分 = 和減差除以二」

  a = (S + D) / 2   ← 把兩個方程式「相加」
  b = (S - D) / 2   ← 把兩個方程式「相減」
"""

import sys


def solve(s: int, d: int) -> str:
    # ┌─────────────────────────────────────────┐
    # │  STEP 1：用聯立方程求解                  │
    # │                                         │
    # │   a + b = S  …(1)                       │
    # │   a - b = D  …(2)                       │
    # │                                         │
    # │  (1)+(2) → 2a = S+D → a = (S+D)//2     │
    # │  (1)-(2) → 2b = S-D → b = (S-D)//2     │
    # └─────────────────────────────────────────┘
    總和  = s + d   # 對應 2a，用來求較大分
    總差  = s - d   # 對應 2b，用來求較小分

    # ── 檢查一：總和必須是偶數，才能整除 ──────────
    # （若為奇數代表 a、b 不是整數，無解）
    if 總和 % 2 != 0:
        return "impossible"

    大分 = 總和 // 2   # 高分隊得分
    小分 = 總差 // 2   # 低分隊得分

    # ── 檢查二：兩隊得分都不能是負數 ──────────────
    # （若小分 < 0，代表 D > S，不合理）
    if 小分 < 0:
        return "impossible"

    # 較大分先輸出，以空格分隔
    return f"{大分} {小分}"


def main():
    # 讀取全部輸入並切割成一串數字（方便依序取用）
    資料 = sys.stdin.read().split()
    位置 = 0

    # 第一行是測試資料組數
    組數 = int(資料[位置]); 位置 += 1

    for _ in range(組數):
        s = int(資料[位置]); 位置 += 1   # 和（Sum）
        d = int(資料[位置]); 位置 += 1   # 差（Difference）
        print(solve(s, d))


if __name__ == "__main__":
    main()
