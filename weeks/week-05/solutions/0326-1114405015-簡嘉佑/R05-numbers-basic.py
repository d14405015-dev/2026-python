# R05. 數字基礎：四捨五入、進制、格式化（3.1–3.4）
# round / Decimal / format / bin / oct / hex

# Decimal 用來做高精度十進位運算，localcontext 可暫時調整精度設定
from decimal import Decimal, localcontext
# math 提供較精確的浮點數加總工具 fsum()
import math

# ── 3.1 四捨五入 ──────────────────────────────────────
# round(數值, 位數) 可指定保留到小數點後幾位
print(round(1.27, 1))  # 1.3
# 保留三位小數時，第四位會參與四捨五入
print(round(1.25361, 3))  # 1.254
# Python 的 round() 對 .5 採用銀行家捨入，會捨到最近的偶數
print(round(0.5))  # 0（銀行家捨入，取最近偶數）
# 2.5 也會捨到最近的偶數 2，而不是一般直覺的 3
print(round(2.5))  # 2

a = 1627731
# 位數使用負數時，代表對整數部分做四捨五入
print(round(a, -2))  # 1627700（對百位四捨五入）

# ── 3.2 精確浮點數 ────────────────────────────────────
# 一般浮點數使用二進位表示，某些十進位小數無法完全精確儲存
print(4.2 + 2.1)  # 6.300000000000001（有誤差）
# Decimal 以十進位做精確運算，適合金額或高精度需求
da, db = Decimal("4.2"), Decimal("2.1")
# 由於 Decimal 是十進位精確表示，因此結果不會出現浮點誤差
print(da + db)  # 6.3（精確）

# localcontext() 可暫時調整 Decimal 的計算精度
with localcontext() as ctx:
    # prec 代表有效位數，這裡示範限制成 3 位
    ctx.prec = 3
    print(Decimal("1.3") / Decimal("1.7"))  # 0.765

# math.fsum 修正大數+小數精度
# 對多個浮點數加總時，math.fsum() 通常比內建 sum() 更準確
print(math.fsum([1.23e18, 1, -1.23e18]))  # 1.0（正確）

# ── 3.3 數字格式化 ────────────────────────────────────
x = 1234.56789
# format(數值, 格式字串) 可以控制小數位數、對齊方式與表示法
# 固定顯示兩位小數
print(format(x, "0.2f"))  # '1234.57'
# >10.1f 表示靠右對齊、總寬度 10、保留 1 位小數
print(format(x, ">10.1f"))  # '    1234.6'
# 加上千分位逗號
print(format(x, ","))  # '1,234.56789'
# 同時使用千分位與小數格式
print(format(x, "0,.2f"))  # '1,234.57'
# 以科學記號表示
print(format(x, "e"))  # '1.234568e+03'

# ── 3.4 二八十六進制 ──────────────────────────────────
n = 1234
# 這一段示範十進位整數與其他進位表示法之間的互相轉換
# bin / oct / hex 會回傳帶有前綴的字串表示法
print(bin(n), oct(n), hex(n))  # 0b10011010010 0o2322 0x4d2
# format() 可只輸出純數字部分，不帶進位前綴
print(format(n, "b"), format(n, "x"))  # 10011010010 4d2
# int(字串, 進位) 可把不同進位的字串轉回十進位整數
print(int("4d2", 16), int("2322", 8))  # 1234 1234
