# UVA 10056 - What is the Probability?（簡單好記版）
#
# ====== 題目核心觀念 ======
# N 個玩家輪流擲骰子，每次成功機率是 p。
# 順序是 1 → 2 → … → N → 1 → 2 → …（一直循環到有人成功為止）。
# 第一個擲出「成功」的人獲勝。
# 題目問：第 i 個玩家獲勝的機率是多少？
#
# ====== 想法：逐輪加總 ======
# 設 q = 1 - p（單次「失敗」的機率）。
#
# 玩家 i 在「第 0 輪」獲勝，需要：
#   前面 (i-1) 個人全部失敗（機率 q^(i-1)），然後玩家 i 成功（機率 p）
#   → 這一輪玩家 i 獲勝的機率 = q^(i-1) × p
#
# 玩家 i 在「第 1 輪」獲勝，需要：
#   第 0 輪所有 N 個人都失敗（機率 q^N），再重複上面的情況
#   → 機率 = q^N × q^(i-1) × p
#
# 玩家 i 在「第 2 輪」獲勝：
#   → 機率 = q^(2N) × q^(i-1) × p
#
# 以此類推，把所有輪次加起來（無窮等比級數，公比 r = q^N）：
#
#   P = q^(i-1)×p  +  q^N × q^(i-1)×p  +  q^(2N) × q^(i-1)×p  + …
#     = q^(i-1) × p × (1 + q^N + q^(2N) + …)
#
# 等比級數公式（公比 r = q^N，|r| < 1 時收斂）：
#   1 + r + r^2 + … = 1 / (1 - r)
#
# 所以最終公式：
#   P(玩家 i 獲勝) = q^(i-1) × p / (1 - q^N)
#
# ====== 特殊情況 ======
# 若 p = 1（每次擲一定成功），玩家 1 第一輪就贏了，機率 = 1。
# 其他玩家根本輪不到，機率 = 0。
#
# ====== 輸出格式 ======
# 機率精確到小數點後 4 位（用 f"{prob:.4f}" 格式化）。

import sys

# 一次讀入全部輸入，切成數字 token，方便依序取值。
tokens = sys.stdin.read().split()
idx = 0

# 第一個數字是測試組數 S。
s = int(tokens[idx])
idx += 1

for _ in range(s):
    # ── 讀取本組輸入 ──────────────────────────────────────────────
    n = int(tokens[idx])    # 玩家總數 N（N ≤ 1000）
    idx += 1
    p = float(tokens[idx])  # 每次擲骰的成功機率 p（0 < p ≤ 1）
    idx += 1
    i = int(tokens[idx])    # 要查詢的玩家序號 i（1 ≤ i ≤ N）
    idx += 1

    # ── 計算失敗機率 ──────────────────────────────────────────────
    # q = 1 - p，代表這一次擲骰「沒有成功」的機率。
    q = 1.0 - p

    # ── 特殊情況：p = 1，玩家 1 必勝 ────────────────────────────
    # 若 p >= 1，每次擲一定成功，玩家 1 在第一輪就獲勝。
    # 玩家 i（i > 1）永遠輪不到，機率為 0。
    if p >= 1.0:
        prob = 1.0 if i == 1 else 0.0

    else:
        # ── 套入等比級數公式 ────────────────────────────────────
        # 分子：玩家 i 前面有 (i-1) 人都失敗的機率，再乘以本次成功機率
        #   = q^(i-1) × p
        numerator = (q ** (i - 1)) * p

        # 分母：1 減去「走完一整輪（N 個人全部失敗）」的機率
        #   = 1 - q^N
        # 這個值代表「遲早會有人成功」的歸一化因子。
        denominator = 1.0 - (q ** n)

        # 最終機率 = 分子 / 分母
        prob = numerator / denominator

    # ── 輸出結果，精確到小數點後 4 位 ───────────────────────────
    print(f"{prob:.4f}")
