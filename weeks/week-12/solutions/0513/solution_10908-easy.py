"""
========================================================
題目：UVA 10908 — Largest Square（簡易好記版）
來源：ZeroJudge a901
========================================================

【這版的設計理念】
  讓程式像「逐步口頭說明」一樣直觀，
  只需記住一句話：

  「從中心往外一圈一圈擴，每圈全部字元相同才算，
    超出邊界或有不同就停，最後邊長 = 圈數×2 + 1」

【一句話記憶】
  不斷嘗試「往外再擴一圈」，直到不能擴為止。

【關鍵變數對應】
  圈數  = h（半徑）  →  0 圈 = 只有中心 1 格（邊長 1）
  邊長  = 2h + 1
  下一圈 = nh = h + 1
"""

import sys


def solve_query(grid, r, c):
    # ── 基本資訊：取得網格大小與中心字元 ──────────────
    總行數 = len(grid)
    總列數 = len(grid[0])
    中心字元 = grid[r][c]   # 要比對的目標字元

    圈數 = 0  # 目前已確認的半徑（0 = 只有中心那 1 格）

    while True:
        下一圈 = 圈數 + 1

        # ── STEP 1：邊界檢查 ───────────────────────────
        # 看看往外再擴一圈後，四個方向是否仍在網格範圍內
        超出上方 = (r - 下一圈 < 0)
        超出下方 = (r + 下一圈 >= 總行數)
        超出左方 = (c - 下一圈 < 0)
        超出右方 = (c + 下一圈 >= 總列數)

        if 超出上方 or 超出下方 or 超出左方 or 超出右方:
            break  # 超出邊界，不能再擴了

        # ── STEP 2：字元檢查 ───────────────────────────
        # 掃描以 (r,c) 為中心、半徑為「下一圈」的正方形
        # 正方形範圍：列 r-下一圈 ~ r+下一圈，行 c-下一圈 ~ c+下一圈
        可以擴 = True
        for 列 in range(r - 下一圈, r + 下一圈 + 1):
            for 行 in range(c - 下一圈, c + 下一圈 + 1):
                if grid[列][行] != 中心字元:
                    可以擴 = False
                    break
            if not 可以擴:
                break

        if not 可以擴:
            break  # 正方形內有不同字元，停止

        # ── STEP 3：擴張成功，更新圈數 ────────────────
        圈數 = 下一圈

    # 邊長 = 圈數 × 2 + 1
    return 2 * 圈數 + 1


def main():
    資料 = sys.stdin.read().split("\n")
    位置 = 0
    T = int(資料[位置]); 位置 += 1   # 測試案例總數

    for _ in range(T):
        # 讀入 M（行數）、N（列數）、Q（查詢數）
        M, N, Q = map(int, 資料[位置].split()); 位置 += 1

        # 讀入 M 行字元網格
        網格 = []
        for _ in range(M):
            網格.append(資料[位置]); 位置 += 1

        # 輸出標頭：M N Q
        print(f"{M} {N} {Q}")

        # 逐一處理每筆查詢
        for _ in range(Q):
            r, c = map(int, 資料[位置].split()); 位置 += 1
            print(solve_query(網格, r, c))


if __name__ == "__main__":
    main()
