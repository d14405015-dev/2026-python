"""
========================================================
題目：UVA 10931 — Parity（奇偶性，簡易好記版）
來源：ZeroJudge a924
========================================================

【這版的設計理念】
  讓程式像「逐步說明」一樣簡單直觀：

  「把十進位數字改成二進位，
    然後數一下有幾個 1，
    最後按格式輸出結果。」

【一句話記憶】
  轉二進位 → 數 1 的個數 → 輸出。

【例子】
  輸入：21
    二進位：10101
    1 的個數：3
    輸出：The parity of 10101 is 3 (mod 2).
"""


def 十進位轉二進位(十進位數字):
    """
    把十進位數轉成二進位字串
    
    例如：
      1 結果是 "1"
      2 結果是 "10"
      10 結果是 "1010"
      21 結果是 "10101"
    """
    # ── Python 的 bin() 會回傳 "0b..." 形式 ──────
    # 例如 bin(5) 回傳 "0b101"
    # 我們只要二進位部分，所以 [2:] 去掉 "0b"
    return bin(十進位數字)[2:]


def 數二進位中有幾個1(十進位數字):
    """
    計算一個數的二進位表示中有幾個 1
    
    例如：
      1 的二進位 "1" → 1 個 1
      2 的二進位 "10" → 1 個 1
      10 的二進位 "1010" → 2 個 1
      21 的二進位 "10101" → 3 個 1
    """
    # ── 轉成二進位後計算 '1' 的個數 ─────────
    # bin(十進位數字) 給 "0b..." 形式
    # .count('1') 數有幾個 '1' 字元
    return bin(十進位數字).count('1')


def solve(十進位數字):
    """
    主要求解函式
    
    功能：
      1. 將十進位轉為二進位字串
      2. 計算二進位中 1 的個數
      3. 按格式輸出結果
    
    輸出格式："The parity of {二進位} is {1的個數} (mod 2)."
    """
    # ── 第一步：轉成二進位 ─────────────
    二進位字串 = 十進位轉二進位(十進位數字)
    
    # ── 第二步：數 1 的個數 ─────────────────
    一的個數 = 數二進位中有幾個1(十進位數字)
    
    # ── 第三步：按格式輸出 ─────────────────
    結果 = f"The parity of {二進位字串} is {一的個數} (mod 2)."
    
    return 結果


def main():
    """
    主程式流程
    
    1. 讀入一行
    2. 轉成整數
    3. 如果是 0，代表結束，停止程式
    4. 否則呼叫 solve() 計算並輸出結果
    5. 重複步驟 1
    """
    while True:
        # ── 讀入一行 ────────────────────
        輸入文字 = input().strip()
        
        # ── 轉成整數 ────────────────────
        輸入數字 = int(輸入文字)
        
        # ── 檢查是否為終止符 0 ──────────────
        if 輸入數字 == 0:
            break
        
        # ── 計算並輸出結果 ──────────────────
        結果 = solve(輸入數字)
        print(結果)


if __name__ == "__main__":
    main()
