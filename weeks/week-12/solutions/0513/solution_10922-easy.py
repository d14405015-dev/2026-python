"""
========================================================
題目：UVA 10922 — 2 the 9s（簡易好記版）
來源：ZeroJudge a915
========================================================

【這版的設計理念】
  讓程式像「逐步說明」一樣簡單直觀：

  「一直把各位數字加起來，加到只剩一個數字為止。
    數一下加了幾次，就是深度。
    最後如果是 9，就是 9 的倍數。」

【一句話記憶】
  往下加總，數次數，最後檢查是否為 9。

【例子】
  189：1+8+9=18（第 1 次）→ 1+8=9（第 2 次）→ 深度 2，答案是 9 的倍數
  100：1+0+0=1（第 1 次）→ 1 是 1 位數，停止 → 不是 9 的倍數
"""

import sys


def 各位加總(數字字串):
    # ── 簡單說明：把所有數字加起來 ──────────────
    # 例如 "189" → 1 + 8 + 9 = 18
    return sum(int(d) for d in 數字字串)


def 判斷和回傳答案(數字字串):
    # ── 開始時 ────────────────────────────────
    # 目前處理的「數字」是輸入的字串
    # 加總次數初始為 0
    目前數字 = 數字字串
    加總次數 = 0

    # ── 重複過程：只要還有多於 1 個數字，就繼續加總 ─
    while len(目前數字) > 1:
        # 計算各位加總
        和 = 各位加總(目前數字)
        # 把結果轉回字串，準備下一輪
        目前數字 = str(和)
        # 計數，這是第幾次加總
        加總次數 += 1

    # ── 最終檢查 ──────────────────────────────
    # 現在 目前數字  一定是 1~9 的某個數字了
    # 如果是 9，就是 9 的倍數
    if 目前數字 == "9":
        return f"9-degree of {數字字串} is {加總次數}."
    else:
        # 否則不是 9 的倍數
        return f"{數字字串} is not a multiple of 9."


# ── 暴露為標準介面供測試使用 ──────────────────
solve = 判斷和回傳答案


def main():
    """讀取輸入，直到輸入 0 為止"""
    while True:
        # 讀入一行
        輸入 = input().strip()
        # 若是 0，停止
        if 輸入 == "0":
            break
        # 否則輸出判斷結果
        print(判斷和回傳答案(輸入))


if __name__ == "__main__":
    main()
